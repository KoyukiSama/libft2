#include "libft.h"
#include <stdlib.h>
#include <stddef.h>

char	*ft_strdup(const char *s)
{
	char	*new_s;
	size_t	i;

	new_s = malloc(ft_strlen(s) + 1);
	if (!new_s)
		return (NULL);
	i = 0;
	while (s[i])
	{
		new_s[i] = s[i];
		i++;
	}
	new_s[i] = '\0';
	return (new_s);
}
